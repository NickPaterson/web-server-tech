Violet (color)
From Wikipedia, the free encyclopedia
As the name of a color, violet (named after the flower violet) is used in two senses: first, referring to the color of light at the short-wavelength end of the visible spectrum, approximately 380–420 nm when indigo is recognized, or more commonly 380–450 nm[3] (this is a spectral color). Second, violet may refer to a shade of purple, that is, a mixture of red and blue light, and not a spectral color (see a discussion of the distinction between violet and purple). Spectral violet is outside the gamut of typical RGB color spaces, and therefore it can be approximated by a purple, but cannot be reproduced exactly on a computer screen.
